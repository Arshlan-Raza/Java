public class switchStatement {
    public static void main(String[] args){
        char alpha = 'c';
        switch (alpha){
            case 'A':
                System.out.println("Its alphabet A");
                break;
            case 'B':
                System.out.println("Its alphabet B");
                break;
            case 'C':
                System.out.println("Its alphabet C");
                break;
            default:
                System.out.println("None of the Above");
        }
    }
}
